#include <gb/gb.h>
#include <stdint.h>
#include "render.h"
#include "tile_defs.h"
#include "world.h"

const uint8_t terrain_tiles[TILE_COUNT * 16u] = {
    0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
    0x00,0x00,0x24,0x24,0x00,0x00,0x42,0x42,0x18,0x18,0x00,0x00,0x24,0x24,0x00,0x00,
    0xFF,0x00,0x00,0xFF,0xAA,0x55,0x00,0xFF,0x24,0x24,0x00,0x00,0x42,0x42,0x18,0x18,
    0x81,0x7E,0x42,0xBD,0x24,0xDB,0x18,0xE7,0x18,0xE7,0x24,0xDB,0x42,0xBD,0x81,0x7E
};

const uint8_t player_tiles[16u] = {
    0x3C,0x3C,0x42,0x7E,0xA5,0xDB,0x81,0xFF,
    0xBD,0xC3,0x99,0xE7,0x42,0x7E,0x24,0x3C
};

void render_init(void)
{
    DISPLAY_OFF;
    set_bkg_data(0u, TILE_COUNT, terrain_tiles);
    set_sprite_data(TILE_COUNT, 1u, player_tiles);
    set_sprite_tile(0u, TILE_COUNT);
    render_world();
    SHOW_BKG;
    SHOW_SPRITES;
    DISPLAY_ON;
}

void render_world(void)
{
    set_bkg_tiles(0u, 0u, WORLD_WIDTH_TILES, WORLD_HEIGHT_TILES, world_tiles());
}

void render_frame(const Camera *camera, const Player *player)
{
    int16_t sprite_x = (int16_t)(player->x - camera->x + 8);
    int16_t sprite_y = (int16_t)(player->y - camera->y + 16);

    scroll_bkg((int16_t)camera->x - SCX_REG, (int16_t)camera->y - SCY_REG);
    move_sprite(0u, (uint8_t)sprite_x, (uint8_t)sprite_y);
}
