static void splash_wait(void)
{
    uint8_t frame;

    for (frame = 0u; frame < SPLASH_DURATION_FRAMES; ++frame) {
        wait_vbl_done();
    }
}

static void draw_splash_robot(void)
{
    set_bkg_data(SPLASH_ROBOT_TILE_BASE,
                 SPLASH_ROBOT_WIDTH * SPLASH_ROBOT_HEIGHT,
                 splash_robot_tiles);
    set_bkg_tiles(7u, 5u, SPLASH_ROBOT_WIDTH, SPLASH_ROBOT_HEIGHT, splash_robot_map);
}

void splash_show(void)
{
    DISPLAY_OFF;
    HIDE_WIN;
    HIDE_SPRITES;

    font_init();
    font_set(font_load(font_ibm));
    mode(get_mode() | M_NO_SCROLL);
    cls();

    gotoxy(2u, 1u);
    printf("E Y E N A U T");
    gotoxy(4u, 3u);
    printf("* ADVENTURES *");

    gotoxy(1u, 5u);
    printf("*    +        .  *");
    gotoxy(2u, 6u);
    printf("  _            o ");
    gotoxy(2u, 7u);
    printf(" /_)             ");
    gotoxy(2u, 8u);
    printf("-'        .      ");

    draw_splash_robot();

    gotoxy(1u, 11u);
    printf("  _________________");
    gotoxy(1u, 12u);
    printf("_/ o  _  o  _  o  \\");

    gotoxy(3u, 14u);
    printf("> PRESS START");
    gotoxy(4u, 16u);
    printf("Angel R. 2026");

    SHOW_BKG;
    DISPLAY_ON;

    splash_wait();
}
